export const mockTimeSlots = (venueId, date) => {
  const slots = []
  const bookedSlots = ['09:00', '10:00', '14:00', '19:00', '20:00']
  for (let hour = 6; hour < 23; hour++) {
    const timeStr = `${hour.toString().padStart(2, '0')}:00`
    const endHour = hour + 1
    const endTimeStr = `${endHour.toString().padStart(2, '0')}:00`
    slots.push({
      id: `${venueId}-${date}-${timeStr}`,
      venue_id: venueId,
      date,
      start_time: timeStr,
      end_time: endTimeStr,
      is_available: !bookedSlots.includes(timeStr),
      price: 3500,
    })
  }
  return slots
}

